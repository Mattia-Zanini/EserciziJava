public class Cerchio{
        public static void main(String[] args){
                double raggio = 10.12;
                double pi = 3.14159265358979;
                double circonferenza = 2*raggio*pi;
                double area = pi*raggio*raggio;
                
                System.out.println("***La circonferenza di un cerchio di raggio "+ raggio + " e' pari a "+ circonferenza +"***");
                System.out.println("***L'area di un cerchio di raggio " + raggio +" e' pari a "+ area +"***");
        }
}
